// ═══════════════════════════════════════════════════════════
// PUBLIC: Admin Register — requires a valid invite token
//
// The invite token is a 64-char hex string generated by
// POST /api/admin/invite (super_admin only). It is stored
// in MongoDB, not in any env var or frontend bundle.
//
// Flow:
//   1. super_admin calls POST /api/admin/invite
//   2. Backend creates AdminInvite record, returns invite URL
//   3. super_admin sends URL to colleague (Slack / email)
//   4. Colleague opens URL → AdminRegister.jsx reads ?invite= from URL
//   5. Colleague submits form → this endpoint validates the token
//   6. Account created as admin + pending — super_admin approves
// ═══════════════════════════════════════════════════════════
router.post("/register", async (req, res) => {
  try {
    await connectDB();

    const invite_token = String(req.body?.invite_token || "").trim();
    const name         = String(req.body?.name         || "").trim();
    const email        = String(req.body?.email        || "").trim().toLowerCase();
    const password     = String(req.body?.password     || "");

    // ── Validate invite token ──────────────────────────────
    if (!invite_token) {
      return res.status(403).json({
        error: "A valid invite link is required to register. Ask a super admin for an invite.",
      });
    }

    const invite = await AdminInvite.findOne({
      token:    invite_token,
      used:     false,
      expiresAt: { $gt: new Date() },
    });

    if (!invite) {
      return res.status(403).json({
        error: "This invite link is invalid or has expired. Ask a super admin for a new one.",
      });
    }

    // ── Validate inputs ────────────────────────────────────
    if (!name)  return res.status(400).json({ error: "Name is required" });
    if (!email) return res.status(400).json({ error: "Email is required" });

    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
      return res.status(400).json({ error: "Valid email is required" });
    }
    if (!password || password.length < 12) {
      return res.status(400).json({ error: "Password must be at least 12 characters" });
    }
    if (!/[A-Z]/.test(password)) {
      return res.status(400).json({ error: "Password must contain at least one uppercase letter" });
    }
    if (!/[0-9]/.test(password)) {
      return res.status(400).json({ error: "Password must contain at least one number" });
    }

    const existing = await Admin.findOne({ email });
    if (existing) {
      return res.status(409).json({ error: "An account with this email already exists" });
    }

    // ── Mark invite as used BEFORE creating admin ──────────
    // (prevents race condition where same link used twice simultaneously)
    invite.used = true;
    await invite.save();

    // ── Hash password explicitly ───────────────────────────
    const password_hash = await bcrypt.hash(password, 12);

    const admin = await Admin.create({
      email,
      name,
      password_hash,
      role:   "admin",     // never super_admin via UI
      status: "pending",   // super_admin must approve
    });

    return res.status(201).json({
      ok:      true,
      pending: true,
      message: "Account created. A super admin must approve it before you can log in.",
      admin:   { name: admin.name, email: admin.email, role: admin.role, status: admin.status },
    });
  } catch (err) {
    console.error("Admin register error:", err);
    return res.status(500).json({ error: "Registration failed" });
  }
});

// ═══════════════════════════════════════════════════════════
// PUBLIC: Admin Login
// ✅ Protected by dedicated rate limiter (5 / 15 min)
// ═══════════════════════════════════════════════════════════
router.post("/login", adminLoginLimiter, async (req, res) => {
  try {
    await connectDB();

    const email    = String(req.body?.email    || "").trim().toLowerCase();
    const password = String(req.body?.password || "");

    if (!email || !password) {
      return res.status(400).json({ error: "Email and password are required" });
    }

    const admin = await Admin.findOne({ email });
    if (!admin) {
      return res.status(401).json({ error: "Invalid email or password" });
    }

    const isValid = await admin.comparePassword(password);
    if (!isValid) {
      return res.status(401).json({ error: "Invalid email or password" });
    }

    if (admin.status === "pending") {
      return res.status(403).json({
        error:  "Your account is pending approval from a super admin.",
        status: "pending",
      });
    }
    if (admin.status === "suspended") {
      return res.status(403).json({
        error:  "Your account has been suspended. Contact a super admin.",
        status: "suspended",
      });
    }

    admin.last_login_at = new Date();
    admin.login_count   = (admin.login_count || 0) + 1;
    await admin.save();

    const token = jwt.sign(
      { adminId: admin._id, email: admin.email, name: admin.name, role: admin.role },
      JWT_SECRET,
      { expiresIn: "2h" },  // ✅ Reduced from 12h to 2h
    );

    // ✅ Set httpOnly cookie + return token for localStorage (hybrid)
    setAuthCookie(res, "admin_token", token, ADMIN_COOKIE_MAX_AGE);

    return res.json({
      ok:    true,
      token,
      admin: { name: admin.name, email: admin.email, role: admin.role, status: admin.status },
    });
  } catch (err) {
    console.error("Admin login error:", err);
    return res.status(500).json({ error: "Login failed" });
  }
});

